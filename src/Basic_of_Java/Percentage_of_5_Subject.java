package Basic_of_Java;

public class Percentage_of_5_Subject {
    public static void main(String[] args) {
        double maths = 97;
        double english = 89;
        double physics = 90;
        double chemistry = 79;
        double biology = 87;

        double percentage = (maths+english+physics+chemistry+biology)/5;
        System.out.println(percentage);
    }
}
